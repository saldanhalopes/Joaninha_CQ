    private void btnRemoverCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverCodigoActionPerformed
        if (tblCodigoBarras.getSelectedRow() != -1) {
            if (tblCodigoBarras.getSelectedRow() == tblCodigoBarras.getRowCount() - 1) {
                DefaultTableModel model = (DefaultTableModel) tblCodigoBarras.getModel();
                model.removeRow(tblCodigoBarras.getSelectedRow());
            } else {
                JOptionPane.showMessageDialog(null, "Não é possivel excluir essa linha");
            }
        }
    }//GEN-LAST:event_btnRemoverCodigoActionPerformed

    private void btnSalvarCadastroProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarCadastroProdutoActionPerformed
        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        int rows = tblCodigoBarras.getRowCount();
        try {
            if (rows <= 0) {
                JOptionPane.showMessageDialog(null, "Cadastrar o Código de Barras!");
            } else {
                for (int row = 0; row < rows; row++) {
                    criarCadastro((Integer) tblCodigoBarras.getValueAt(row, 0),
                            (Integer) tblCodigoBarras.getValueAt(row, 1));
                }
                dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi possivel salvar. Algum Campo está inválido!");
        }
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }//GEN-LAST:event_btnSalvarCadastroProdutoActionPerformed

