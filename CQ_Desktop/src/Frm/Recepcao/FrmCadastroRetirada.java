    private void verificarCodigo() {
        String in = txtCodigoBarras.getText();
        in = in.replaceFirst("^0+(?!$)", "");
        DefaultTableModel model = (DefaultTableModel) tblCodigoBarras.getModel();
        int rows = tblCodigoBarras.getRowCount();
        for (int row = 0; row < rows; row++) {
            if (in.equals(tblCodigoBarras.getValueAt(row, 1).toString())) {
                pnlRA.setVisible(false);
                btnAdicionarCodigo.setEnabled(true);
                btnRemoverCodigo.setEnabled(true);
                txtCodigoBarras.setText(null);
                JOptionPane.showMessageDialog(null, "Código já adicionado!");
                return;
            }
        }
        if (!txtCodigoBarras.getText().isEmpty()) {
            model.addRow(new Object[]{
                rows + 1,
                in
            });
        }
        pnlRA.setVisible(false);
        btnAdicionarCodigo.setEnabled(true);
        btnRemoverCodigo.setEnabled(true);
        txtCodigoBarras.setText(null);
    }

