    private void retirarLoteAcompanhamento(Usuario user) {
        LoteAcompanhamento loteacomp = new LoteAcompanhamento();
        LoteAcompanhamentoDAO loteacompDAO = new LoteAcompanhamentoDAO();
        Lote lote = new Lote();
        LoteDAO loteDAO = new LoteDAO();
        lote.setLote_status("Em An√°lise");
        loteacomp.setLote_acompanhamento_id(acomp_id);
        loteacomp.setData_retirada(DataHora.getTimestampDate(new Date()));
        loteacomp.setUser_retirada(user);
        loteacompDAO.updateRetiradaLoteAcompanhamento(loteacomp);
        loteDAO.updateLoteStatus(lote);
    }
    
    private void devolverLoteAcompanhamento() {
        LoteAcompanhamento loteacomp = new LoteAcompanhamento();
        LoteAcompanhamentoDAO loteacompDAO = new LoteAcompanhamentoDAO();
        loteacomp.setLote_acompanhamento_id(acomp_id);
        loteacompDAO.updateDevolverLoteAcompanhamento(loteacomp);
    }
    
