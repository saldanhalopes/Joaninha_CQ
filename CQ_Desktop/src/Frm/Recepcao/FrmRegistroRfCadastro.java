    private void read() {
        RegistroRf regrf = new RegistroRf();
        RegistroRfDAO regrfDAO = new RegistroRfDAO();
        if (registro_id == null) {
            regrfDAO.selectUltimaCaixa(regrf);
            if (regrf.getData_fim() == null) {
                txtCaixa.setText(regrf.getCaixa());
                caixa_id = regrf.getCaixa_rf_id();
            }
        }
    }

