    private void tblRegistroRfKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblRegistroRfKeyReleased
        if (tblRegistroRf.getSelectedRow() != -1) {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                FrmRegistroRfCadastro frm = new FrmRegistroRfCadastro(null, true,
                        (Integer) tblRegistroRf.getValueAt(tblRegistroRf.getSelectedRow(), 0),
                        (Integer) tblRegistroRf.getValueAt(tblRegistroRf.getSelectedRow(), 1),
                        (Integer) tblRegistroRf.getValueAt(tblRegistroRf.getSelectedRow(), 2));
                frm.setVisible(true);
                readRegistroRf();
            }
            if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
                deletarRegistroRf((Integer) tblRegistroRf.getValueAt(tblRegistroRf.getSelectedRow(), 0));
                readRegistroRf();
            }
        }
    }//GEN-LAST:event_tblRegistroRfKeyReleased

