    private void readRegistroRf() {
        DefaultTableModel model = (DefaultTableModel) tblRegistroRf.getModel();
        RegistroRfDAO regrfDAO = new RegistroRfDAO();
        model.setNumRows(0);
        for (RegistroRf regrf : regrfDAO.readRegistroRf(Integer.valueOf(cmbLimitPesquisar.getSelectedItem().toString()))) {
            model.addRow(new Object[]{
                regrf.getRegistro_rf_id(),
                regrf.getCaixa_rf_id(),
                regrf.getMaterial().getMaterial_id(),
                regrf.getLote(),
                regrf.getCaixa(),
                regrf.getMaterial().getCod_material(),
                regrf.getMaterial().getNome_material(),
                regrf.getCentro_custo(),
                regrf.getDeposito(),
                regrf.getQuantidade(),
                DataHora.getStringDateTime(regrf.getData_registro()),
                regrf.getUser_registro().getUser()
            });
        }
    }

    private void deletarRegistroRf(Integer id) {
        RegistroRfAcesso Acesso = new RegistroRfAcesso();
        if (Acesso.verificarDeletarDadosRegistroRf()) {
            RegistroRf regrf = new RegistroRf();
            RegistroRfDAO regrfDAO = new RegistroRfDAO();
            regrf.setRegistro_rf_id(id);
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            FrmSalvar frmSalvar = new FrmSalvar(null, true);
            frmSalvar.setVisible(true);
            if (FrmSalvar.senha()) {
                regrfDAO.deleteRegistroRf(regrf);
            }
        }
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }

