    private void txtPesquisarMetodologiaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisarMetodologiaKeyReleased
        DefaultTableModel model = (DefaultTableModel) tblRegistroAcompanhamento.getModel();
        TableRowSorter sorter = new TableRowSorter<>(model);
        tblRegistroAcompanhamento.setRowSorter(sorter);
        String text = txtPesquisarMetodologia.getText().trim();
        String parametro = "*";
        if (text.length() == 0) {
            sorter.setRowFilter(null);
        } else if (text.indexOf(parametro) > 0) {
            ArrayList<RowFilter<Object, Object>> andFilters = new ArrayList<>();
            andFilters.add(RowFilter.regexFilter("(?i)" + text.substring(0, text.indexOf(parametro)).trim().toUpperCase()));
            andFilters.add(RowFilter.regexFilter("(?i)" + text.substring(text.indexOf(parametro) + 1, text.length()).trim().toUpperCase()));
            sorter.setRowFilter(RowFilter.andFilter(andFilters));
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
        }
    }//GEN-LAST:event_txtPesquisarMetodologiaKeyReleased

    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        int[] rows = tblRegistroAcompanhamento.getSelectedRows();
        if (tblRegistroAcompanhamento.getSelectedRow() != -1) {
            for (int num : rows) {
                Metodologia mtd = new Metodologia();
                Setor setor = new Setor();
                Acompanhamento acomp = new Acompanhamento();
                acomp.setRegistro_acompanhamento_id((Integer) tblRegistroAcompanhamento.getValueAt(num, 0));
                acomp.setRegistro_acompanhamento(tblRegistroAcompanhamento.getValueAt(num, 1).toString());
                mtd.setCod_metodo(tblRegistroAcompanhamento.getValueAt(num, 2).toString());
                setor.setSigla_setor(tblRegistroAcompanhamento.getValueAt(num, 3).toString());
                acomp.setMetodo(mtd);
                acomp.setSetor(setor);
                acomp.setVersao((Integer) tblRegistroAcompanhamento.getValueAt(num, 4));
                acompanhamentos.add(acomp);
            }
            dispose();
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

