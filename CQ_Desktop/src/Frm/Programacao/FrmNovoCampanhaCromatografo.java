    private void criarCampanha(String metodos) {
        try {
            Campanha camp = new Campanha();
            CampanhaDAO campDAO = new CampanhaDAO();
            Cromatografo equip = new Cromatografo();
            Analise anls = new Analise();
            Setor setor = new Setor();
            camp.setNome_campanha(txtNomeCampanha.getText().toUpperCase());
            camp.setMetodos(metodos);
            equip.setSystem_name(cmbEquipamento.getSelectedItem() != null
                    ? cmbEquipamento.getSelectedItem().toString() : "");
            camp.setCromatografo(equip);
            camp.setObs_campanha(txtObs.getText().toUpperCase());
            camp.setOrdem(campDAO.selectUltimaCampanhaByOrdem() + 1);
            setor.setSigla_setor(cmbSetorCampanhas.getSelectedItem() == null ? ""
                    : cmbSetorCampanhas.getSelectedItem().toString());
            camp.setSetor(setor);
            anls.setAnalise_id(ComboBox.getKeyForValue(cmbAnalise.getSelectedItem().toString(), categoryMapAnalise));
            camp.setAnalise(anls);
            Timestamp datahora_previsao = DataHora.getTimestampDateTime(DataHora.getStringDate(txtDataPrevisao.getDate())
                    + " " + txtHora.getValue().toString()
                    + ":" + txtMinuto.getValue().toString() + ":00");
            camp.setPrevisao(datahora_previsao);
            camp.setCor(cmbCor.getSelectedItem().toString());
            campDAO.createCampanhas(camp);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar dados: " + e);
        }
    }

