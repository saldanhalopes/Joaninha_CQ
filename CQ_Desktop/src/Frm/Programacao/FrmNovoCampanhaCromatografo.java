    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNomeCampanha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nome da Campanha inválido");
            txtNomeCampanha.requestFocus();
        } else if (cmbSetorCampanhas.getSelectedItem() == null
                ? true : cmbSetorCampanhas.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Setor inválido");
            cmbSetorCampanhas.requestFocus();
        } else if (cmbAnalise.getSelectedItem() == null
                ? true : cmbAnalise.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Análise inválida");
            cmbAnalise.requestFocus();
        } else {
            StringBuilder sb = new StringBuilder();
            sb.append("a").append(txtCodMetodo1.getText());
            sb.append("b").append(txtCodMetodo2.getText());
            sb.append("c").append(txtCodMetodo3.getText());
            sb.append("d").append(txtCodMetodo4.getText());
            FrmSalvar frmSalvar = new FrmSalvar(null, true);
            frmSalvar.setVisible(true);
            if (frmSalvar.senha()) {
                setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                if (editar || editar_campanha) {
                    atualizarCampanha(sb.toString());
                } else {
                    criarCampanha(sb.toString());
                }
                setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR)
                );
                dispose();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

