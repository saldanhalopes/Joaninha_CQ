    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null,
                "Deseja enviar relatorio do Status dos Equipamentos?",
                "Aviso", JOptionPane.YES_NO_OPTION);
        if (dialogResult == 0) {
            final FrmCarregando frm = new FrmCarregando();
            frm.setVisible(true);
            Thread t1 = new Thread() {
                @Override
                public void run() {
                    setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                    String where = "";
                    if (!"Todos".equals(setores)) {
                        where = " AND tb_setor.sigla_setor = '"
                                + setores + "' ";
                    }
                    int rows = tblUsuarios.getRowCount();
                    for (int row = 0; row < rows; row++) {
                        Usuario user = new Usuario();
                        UsuarioDAO userDAO = new UsuarioDAO();
                        user.setUser_id((Integer) tblUsuarios.getValueAt(row, 0));
                        userDAO.selectUser(user);
                        CampanhaDAO.sendToStatusEquipamento(user, where);
                    }
                    frm.dispose();
                    setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                }
            };
            t1.start();
            dispose();
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

