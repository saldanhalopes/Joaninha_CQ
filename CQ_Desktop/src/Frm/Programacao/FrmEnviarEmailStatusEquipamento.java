    private void tblUsuariosKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblUsuariosKeyPressed
        if (tblUsuarios.getSelectedRow() != -1) {
            if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
                DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
                model.removeRow(tblUsuarios.getSelectedRow());
            }
        }
    }//GEN-LAST:event_tblUsuariosKeyPressed

    private void btnAdicionarDestinatarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarDestinatarioActionPerformed
        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        int rows = tblUsuarios.getRowCount();
        DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
        List<Usuario> users = new ArrayList<>();
        FrmSelecionaUsuario frm = new FrmSelecionaUsuario(null, true, users);
        frm.setVisible(true);
        outerloop:
        for (Usuario user : users) {
            for (int row = 0; row < rows; row++) {
                if ((Integer) tblUsuarios.getValueAt(row, 0) == user.getUser_id()) {
                    JOptionPane.showMessageDialog(null, "UsuÃ¡rio jÃ¡ adicionado!");
                    continue outerloop;
                }
            }
            model.addRow(new Object[]{
                user.getUser_id(),
                user.getUser(),
                user.getName(),
                user.getEmail()
            });
        }
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }//GEN-LAST:event_btnAdicionarDestinatarioActionPerformed

